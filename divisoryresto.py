"""Escriba un programa que pida dos números enteros y muestre el resultado de la división entera y el resto.
"""

num_1= int(input("Ingrese el primer numero:"))
num_2= int(input("Ingrese el segundo numero:"))


print(f"el resultado de la division entera es: {num_1//num_2} ")
print(f"el resultado del resto de la division  es: {num_1 % num_2} ")